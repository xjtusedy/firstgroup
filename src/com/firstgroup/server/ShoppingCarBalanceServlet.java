	/**
	 * The doGet method of the servlet. <br>
	 *
	 * This method is called when a form has its tag value method equals to get.
	 * 
	 * @param request the request send by the client to the server
	 * @param response the response send by the server to the client
	 * @throws ServletException if an error occurred
	 * @throws IOException if an error occurred
	 */
	public void doGet(HttpServletRequest request, HttpServletResponse response)
			throws ServletException, IOException {

		response.setContentType("text/html;charset=utf-8");
		request.setCharacterEncoding("utf-8");
		PrintWriter out = response.getWriter();
		String userName = request.getParameter("username");
		String goodsName = request.getParameter("goodsname");
		System.out.println("结算商品为："+goodsName);
		String getInTime = request.getParameter("getintime");
		String shopTime = request.getParameter("shoptime");
		String number = request.getParameter("number");
		boolean flag = false;
		ShoppingCarDao shoppingCarDao = new ShoppingCarDao();
		try {
			flag=shoppingCarDao.balanceShoppingCar(userName, goodsName, shopTime, number, getInTime);
		} catch (SQLException e) {
			// TODO Auto-generated catch block
			e.printStackTrace();
		}
		if(flag) {
			out.write("true");
			System.out.println("结算成功！");
		}
		else {
			out.write("false");
			System.out.println("结算失败!");
		}
		out.flush();
		out.close();
	}

